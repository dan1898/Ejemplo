package SegundoProyecto;

public class cpos {
private int n;
private String s;
private float f;
private String os;
private char c;
private byte b;
public int getN() {
	return n;
}
public void setN(int n) {
	this.n = n;
}
public cpos(int n, String s, float f, String os, char c, byte b) {
	super();
	this.n = n;
	this.s = s;
	this.f = f;
	this.os = os;
	this.c = c;
	this.b = b;
}
public String getS() {
	return s;
}
public void setS(String s) {
	this.s = s;
}
public float getF() {
	return f;
}
public void setF(float f) {
	this.f = f;
}
public String getOs() {
	return os;
}
public void setOs(String os) {
	this.os = os;
}
public char getC() {
	return c;
}
public void setC(char c) {
	this.c = c;
}
public byte getB() {
	return b;
}
public void setB(byte b) {
	this.b = b;
}
}
